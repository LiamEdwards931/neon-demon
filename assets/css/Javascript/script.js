function openNav() {
    document.getElementById("mySidebar").style.width = "250px";
    document.getElementById("#back-to-top-button").style.marginLeft = "250px";
  }
  
  function closeNav() {
    document.getElementById("mySidebar").style.width = "0";
    document.getElementById("back-to-top-button").style.marginLeft= "0";
  }